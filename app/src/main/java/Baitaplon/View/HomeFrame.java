    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String Ca = "";
        if(RBSang.isSelected()){
            Ca = "Sáng";
        }
        if(RBToi.isSelected()){
            Ca = "Tối";
        }
        if(RBChieu.isSelected()){
            Ca = "Chiều";
        }
        if(TextTenNV.getText().trim().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Không được để trống tên nhân viên.");
        }else if(TexttuoiNV.getText().trim().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Không được để trống tuổi nhân viên.");
        }else if(TextEmailNV.getText().trim().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Không được để trống Email Nhân Viên");
        }else if(TextDCNV.getText().trim().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Không được để trống địa chỉ");
        }else if(Ca.trim().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa chọn ca làm việc cho Nhân Viên");
        }else if(TextLuong.getText().trim().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Không được để trống hệ số lương");
        }else{
            float heluong;
            int tuoi;
            try {
                heluong = Float.parseFloat(TextLuong.getText());
                try {
                    tuoi = Integer.parseInt(TexttuoiNV.getText().trim());
                    int n = ArrNhanVien.size();
                    n=100+n;
                    String ma = "NVI"+n;
                    NhanVien nv = new NhanVien(ma,heluong, Ca, TextTenNV.getText().trim(), tuoi, 
                    TextEmailNV.getText().trim(), TextDCNV.getText().trim());
                    ArrNhanVien.add(nv);
                    showdata(ArrNhanVien, modelB);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(rootPane, "Tuổi phải nhập số nguyên");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Hệ số lương phải nhập số");
            }
            
        }
    }//GEN-LAST:event_jButton6ActionPerformed


    public <T> void showdata(ArrayList<T> list,DefaultTableModel model){
        model.setRowCount(0);
        for(T values : list){
            if(values instanceof Phong){
                Phong ph = (Phong) values;
                model.addRow(new Object[]{
                    ph.getMaph(),ph.getArrghe().size(),ph.getArrphim().size()
                });
            }if(values instanceof NhanVien){
                NhanVien nv = (NhanVien) values;
                model.addRow(new Object[]{
                    nv.getManv(),nv.getHoten(),nv.getTuoi(),nv.getEmail(),nv.getCalam(),
                    nv.getHesoluong(),nv.getDiachi()
                });
            }if(values instanceof KhachHang){
                KhachHang kh = (KhachHang) values;
                model.addRow(new Object[]{
                    kh.getID(),kh.getHoten(),kh.getTuoi(),kh.getEmail(),
                    kh.getDiachi(),kh.getTenTK(),kh.getPass()
                });
            }
        }
    }
